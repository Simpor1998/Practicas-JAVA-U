public class EjemploIfElse {
    public static void main(String[] args) {
        int nota = 2;

        // Evalúa la condición y ejecuta uno u otro bloque
        if (nota >= 3) {
            System.out.println("¡Aprobaste!");
        } else {
            System.out.println("Reprobaste.");
        }
    }
}
